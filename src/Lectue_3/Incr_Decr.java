package Lectue_3;

public class Incr_Decr {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a++);
    }
}
